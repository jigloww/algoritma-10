package Praktikum14;

public class Customer09 {
    public int id;
    public String name;

    public Customer09() {

    }
    
    public Customer09(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public String toString() {
        return " ID: " + this.id + " Nama: " + this.name;
    }
}
